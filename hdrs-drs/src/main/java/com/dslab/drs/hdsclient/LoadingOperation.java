package com.dslab.drs.hdsclient;

/**
 *
 * @author caca
 */
public class LoadingOperation {

  private String method;
  private String dealCount;
  private String dealBytes;
  private String pastCount;
  private String pastBytes;

  public String getMethod() {
    return this.method;
  }

  public String getDealCount() {
    return this.dealCount;
  }

  public String getDealBytes() {
    return this.dealBytes;
  }

  public String getPastCount() {
    return this.pastCount;
  }

  public String getPastBytes() {
    return this.pastBytes;
  }

}
